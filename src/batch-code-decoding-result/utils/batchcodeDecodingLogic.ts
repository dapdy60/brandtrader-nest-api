  private decoders: { [key: string]: (batchCode: string) => Promise<string> };

  constructor() {
    // Dynamically bind all methods from BatchCodeDecoder to decoders with corresponding brand names
    this.decoders = this.getDecoderMapping();
  }

  public async smartDecoderLookup(batchCode: string): Promise<DecodeResult> {
    let foundResult = false;
    let result = new DecodeResult();
    let name : string;
    for (const [brand, decodeMethod] of Object.entries(this.decoders)) {
      try {
        result = JSON.parse(await decodeMethod(batchCode));
        name = decodeMethod.name;
        if (result && !result.output.includes("Invalid batch code") && (result.year) && !result.output.includes("undefined") ) {
          foundResult = true;
          break;
        }
      } catch (error) {
        continue;
      }
    }
    if (!foundResult) {
      return {
        output: "Invalid batch code",
        hasError: true,
        errorMessage: "Invalid batch code"
      };
    }else{
      return {
        ...result,
        usedFallback: true,
        functionUsed: name
      };
    }
  }
  

  public async decodeBatch(item: BatchCodeDecoderDto): Promise<DecodeResult> {
    const { brand, batchCode } = item;
    if (brand && this.decoders[brand]) {
      const decodedInfo: DecodeResult = JSON.parse(await this.decoders[brand](batchCode));
        return { ...decodedInfo, usedFallback: false };
    }
    return {
      output: "Invalid batch code",
      hasError: true,
      errorMessage: "Invalid batch code"
    };
    
  }
  

  public async bulkDecodeBatch(batchCodeList: BatchCodeDecoderDto[]): Promise<DecodeResult[]> {
    const results: Promise<DecodeResult>[] = batchCodeList.map(item => this.decodeBatch(item));
    return Promise.all(results);
  }

